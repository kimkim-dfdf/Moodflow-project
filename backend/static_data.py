def get_books_by_tags(tag_slugs, limit):
    if not tag_slugs:
        if limit:
            return BOOK_RECOMMENDATIONS[:limit]
        return BOOK_RECOMMENDATIONS
    
    result = []
    for book in BOOK_RECOMMENDATIONS:
        match_count = 0
        for tag in tag_slugs:
            if tag in book['tags']:
                match_count = match_count + 1
        if match_count == len(tag_slugs):
            book_copy = dict(book)
            book_copy['match_score'] = match_count / len(book['tags']) * 100
            result.append(book_copy)
    
    for i in range(len(result)):
        for j in range(i + 1, len(result)):
            if result[j]['match_score'] > result[i]['match_score']:
                temp = result[i]
                result[i] = result[j]
                result[j] = temp
    
    if limit:
        return result[:limit]
    return result


def get_tag_objects_for_book(book):
    result = []
    for tag_slug in book['tags']:
        for tag in BOOK_TAGS:
            if tag['slug'] == tag_slug:
                result.append(tag)
    return result
